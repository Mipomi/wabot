async function translateText(text, targetLanguage = 'en') {
    try {
        const response = await axios.get('https://translate.googleapis.com/translate_a/single', {
            params: {
                client: 'gtx',
                sl: 'auto',
                tl: targetLanguage,
                dt: 't',
                q: text
            }
        });

        return response.data[0][0][0];
    } catch (error) {
        console.error('Kesalahan dalam menerjemahkan teks:', error);
        throw new Error('Tidak dapat menerjemahkan teks');
    }
}

